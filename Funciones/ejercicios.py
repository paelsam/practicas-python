def desplegarNombre(nombres):
    for nombre in nombres:
        print(nombre)

nombres = ["Juan", "Karla", "Daniel"]
desplegarNombre(nombres)
desplegarNombre("Elkin")
desplegarNombre((8,9))
desplegarNombre([10, 11])

